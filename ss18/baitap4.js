switch(number){
    case 0:
        console.log('số không');
        break;
    case 1:
        console.log('số một');
        break;
    case 2:
        console.log('số hai');
        break;
    case 3:
        console.log('số ba');
        break;
    case 4:
        console.log('số bốn');
        break;
    case 5:
        console.log('số năm');
        break;
    case 6:
        console.log('số sáu');
        break;
    case 7:
        console.log('số bảy');
        break;
    case 8:
        console.log('số tám');
        break;
    case 9:
        console.log('số chín');
        break;
    default:
        console.log('số không hợp lệ');
        break;
}